//RETURN VALUE WITH FUNCTION ONLY
function ambil () {

    return "terambil";
}

console.log(ambil());

//RETURN VALUE WITH FUNCTION AND VARIABLE
function satu () {
var angka = "1,2,3,4,5";
return angka
}

console.log(satu());

//RETURN VALUE DENGAN PARAMETER
function bola() {
    var sepak = "ditendang";
    return sepak;
}
var football = bola();
